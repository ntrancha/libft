int			ft_facto(int nb)
{
	int		i;
	int		ret;

	i = 0;
	ret = nb;
	if (nb == 1 || nb == 0)
		return (1);
	if (nb >= 13 || nb < 0)
		return (0);
	while (++i < nb)
		ret *= (nb - i);
	return (ret);
}
